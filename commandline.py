# Navigating Directories:

'''cd: Change directory.
ls (on Unix-based systems) or dir (on Windows): List directory contents.'''

# File Operations:

'''cp: Copy files or directories.
mv: Move or rename files or directories.
rm (on Unix-based systems) or del (on Windows): Remove/delete files.'''


# Working with Environments and Packages:
# pip: Package installer for Python. You can use it to install, uninstall, or manage Python packages.

'''pip install package_name
pip uninstall package_name'''


# Text Editing:

'''nano, vim, emacs, or code (Visual Studio Code): Text editors for creating and editing files directly from the command line.'''


# Version Control:
# git: Version control system for tracking changes in source code.

'''git clone repository_url
git add filename
git commit -m "Commit message"
git push'''


# Remote Connections:
# ssh: Secure Shell for remote connections.

'''ssh username@hostname'''


# System Monitoring and Resource Management:

'''top (on Unix-based systems): Displays system processes and their resource usage.
htop (an enhanced version of top): Provides an interactive and visually appealing process viewer.'''

# Data Transfer:
# scp: Secure copy for securely copying files between local and remote hosts.

'''scp local_file.txt username@remote_host:/path/to/destination'''
